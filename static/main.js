function runInstallStep() {
    if (stepIndex >= installSteps.length) {
        postInstallMessage();
        return;
    }

    const step = installSteps[stepIndex];
    let progress = 0;
    term.write(`${step.text} ${drawProgressBar(0)}`);

    const interval = setInterval(() => {
        if (progress >= 100) {
            clearInterval(interval);
            term.writeln(''); // РҝРөСҖРөРІРҫРҙРёРј СҒСӮСҖРҫРәСғ
            stepIndex++;
            setTimeout(runInstallStep, 200);
        } else {
            progress += Math.floor(Math.random() * 12) + 8; // 8-20%
            if (progress > 100) progress = 100;
            term.write(`\r${step.text} ${drawProgressBar(progress)}`);
        }
    }, step.duration / 8);
}

// ------------------- РҡРҫРјР°РҪРҙРҪР°СҸ РҫРұРҫР»РҫСҮРәР° -------------------
function writePrompt() {
    if (commandsEnabled) {
        term.write(prompt);
    }
}

function handleCommand(cmd) {
    cmd = cmd.trim().toLowerCase();
    if (cmd === 'help') {
        return 'Available commands: help, clear, echo [text]';
    } else if (cmd === 'clear') {
        term.clear();
        return null;
    } else if (cmd.startsWith('echo ')) {
        return cmd.slice(5);
    } else if (cmd === '') {
        return '';
    } else {
        return `Command not found: ${cmd}. Type 'help' for list.`;
    }
}

term.onData(e => {
    if (!commandsEnabled) return;

    if (e === '\r') { // Enter
        term.write('\r\n');
        const output = handleCommand(currentLine);
        if (output !== null) {
            term.writeln(output);
        }
        currentLine = '';
        writePrompt();
    } else if (e === '\x7f') { // Backspace
        if (currentLine.length > 0) {
            currentLine = currentLine.slice(0, -1);
            term.write('\b \b');
        }
    } else if (e >= ' ' && e <= '~') {
        currentLine += e;
        term.write(e);
    }
});

